CREATE OR REPLACE
FUNCTION sys_seq(seq_name varchar2)
return number is
pragma  AUTONOMOUS_TRANSACTION;
seq_maxValue number:=0;
begin
 UPDATE turing_std_sys_seq SET CURRENT_VALUE=(select MAX(CURRENT_VALUE+ PERIOD) from turing_std_sys_seq where NAME=seq_name) where NAME=seq_name;
 commit;
 select max(CURRENT_VALUE) into seq_maxValue from turing_std_sys_seq where NAME=seq_name;
 return seq_maxValue;
end;
